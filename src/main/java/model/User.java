package model;

public class User {
    private String userId;       // Unique ID (e.g., C001, L001)
    private String email;        // User's email address
    private String name;         // User's full name
    private String password;     // User's password
    private String role;         // Role (Student, Lecturer, Admin, MediaStaff)
    private String department;   // Department (For Lecturers, Students)
    private String course;       // Course (For Students)
    private Integer year;        // Year (For Students)

    // Getters and Setters
    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }
}
